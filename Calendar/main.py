    def closeEvent(self, event):
        if settings_handler.get("g","exitDialog")=="True":
            m=guiTools.ExitApp(self)
            m.exec()
            if m:
                event.ignore()
        else:
            self.close()
    def on_convert(self):
        index=self.select.currentIndex()
        year=self.year.value()
        month=self.month.value()
        day=self.day.value()
        if index==0:
            result=convert.Gregorian(year,month,day).to_hijri()
        else:
            result=convert.Hijri(year,month,day).to_gregorian()
        qt.QMessageBox.information(self,_("result"),str(result))
